public class kalkulator {
    public static void main(String[] args) {
        double mm = 35.5;
        double cm = 10;

        double calcius =87.67;
        double f =33.800;

        double km = 56.99;
        double b =0.62;

        double x1,x2,x3;

        x1 = mm / cm;
        x2 = calcius * f;
        x3 = km * b ;

        System.out.println("35.5 Milimeter kepada Centimeter ialah : " +x1);
        System.out.println("87.67 Calcius kepada Fahrenheit ialah : " +x2);
        System.out.println("56.99 Kilometer kepada Batu ialah : " +x3);

        
        
    }
    
}
